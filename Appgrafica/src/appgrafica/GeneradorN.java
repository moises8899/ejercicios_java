    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sp1 = String.valueOf(jSpinner1.getValue());
        String sp2 = String.valueOf(jSpinner2.getValue());
        int na = Integer.parseInt(sp1);
        int nb = Integer.parseInt(sp2);

        //comprobamos que el numero2 sea mayor que el num1
        if (nb <= na) {
            //Si el num1 es mayor o igual al num2 dará un mensaje de error
            JOptionPane.showMessageDialog(null, "Número 2 tiene que ser mayor que número 1", "Atención", JOptionPane.ERROR_MESSAGE);
        } else {
            int nat = (int) (Math.random() * (nb - na + 1) + na);
            String ntex = String.valueOf(nat);
            jTextField1.setText(ntex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed


